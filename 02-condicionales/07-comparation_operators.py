

# Igualdad  ==
print(5 == 5) # True
print(5 == 3) # False
print(5 == "5") # False

print("\n")

# Desigualdad !=
print(5 != 5) # False
print(5 != 3) # True
print(5 != "5") # True

print("\n")

# Mayor que >=
print(5 > 5) # False
print(5 > 1) # True 
print(5 > 6) # False
print(5 >= 5) # True
print(5 >= 6) # False

print("\n")

# Menor que < o <=
print(5 < 5) # False
print(5 < 1) # False
print(5 < 6) # True
print(5 <= 5) # True
print(5 <= 6) # True