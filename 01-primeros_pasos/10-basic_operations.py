

# Suma
print(2 + 2) # 4

# Resta 
print(2 - 2) # 0

# Producto o multiplicación
print(2 * 2) # 4

# Modulo
print(1 % 2) # 0.5? NO, Nos daría 1

# División (con decimal)
print(2 / 2) # 1.0

# División (sin decimal)
print(3 // 4) # 0   

# Potencia 
print(2 ** 3) # 8

# Redondear
print(round(3.4)) # 3
print(round(3.5)) # 4

# Valor absoluto abs
print(abs(-1))
print(abs(0))
print(abs(7))